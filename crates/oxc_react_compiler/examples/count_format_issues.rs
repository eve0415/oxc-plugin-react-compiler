fn main() {
    let fixture_dir = "third_party/react/compiler/packages/babel-plugin-react-compiler/src/__tests__/fixtures/compiler";
    let options = oxc_react_compiler::options::PluginOptions::default();
    let mut would_fix = 0;
    let mut partial_help = 0;

    for entry in std::fs::read_dir(fixture_dir).unwrap() {
        let entry = entry.unwrap();
        let path = entry.path();
        let ext = path.extension().and_then(|e| e.to_str());
        match ext {
            Some("js" | "jsx" | "ts" | "tsx") => {}
            _ => continue,
        }

        let stem = path.file_stem().unwrap().to_string_lossy().to_string();
        let expect_path = std::path::Path::new(fixture_dir).join(format!("{stem}.expect.md"));
        if !expect_path.exists() {
            continue;
        }

        let source = std::fs::read_to_string(&path).unwrap();
        let expect_md = std::fs::read_to_string(&expect_path).unwrap();
        let Some(expected_code) = extract_code_block(&expect_md) else {
            continue;
        };
        if !expected_code.contains("_c(") {
            continue;
        }

        let filename = path.file_name().unwrap().to_string_lossy().to_string();
        let result = oxc_react_compiler::compile(&filename, &source, &options);
        if !result.transformed {
            continue;
        }

        let actual_old = normalize_code_old(&result.code);
        let expected_old = normalize_code_old(&expected_code);
        if actual_old == expected_old {
            continue;
        } // Already passing

        // Try with better normalization
        let actual_new = normalize_code_new(&result.code);
        let expected_new = normalize_code_new(&expected_code);

        if actual_new == expected_new {
            would_fix += 1;
        } else if actual_old != actual_new || expected_old != expected_new {
            // The normalization changed something, so it helped partially
            let old_diff = compute_diff(&actual_old, &expected_old);
            let new_diff = compute_diff(&actual_new, &expected_new);
            if new_diff < old_diff {
                partial_help += 1;
            }
        }
    }

    eprintln!("Would fix (new passes): {}", would_fix);
    eprintln!("Partial help (fewer diff lines): {}", partial_help);
}

fn compute_diff(a: &str, e: &str) -> usize {
    let al: Vec<&str> = a.lines().collect();
    let el: Vec<&str> = e.lines().collect();
    let mut diff = 0;
    for i in 0..al.len().max(el.len()) {
        if i >= al.len() || i >= el.len() || al[i] != el[i] {
            diff += 1;
        }
    }
    diff
}

fn normalize_code_old(code: &str) -> String {
    code.lines()
        .map(|line| {
            let trimmed = line.trim();
            let mut s = trimmed.to_string();
            s = s.replace('\'', "\"");
            s = normalize_destructuring(&s);
            s
        })
        .filter(|l| !l.is_empty())
        .collect::<Vec<_>>()
        .join("\n")
}

fn normalize_code_new(code: &str) -> String {
    code.lines()
        .map(|line| {
            let trimmed = line.trim();
            let mut s = trimmed.to_string();
            s = s.replace('\'', "\"");
            // Normalize all {key: value} to { key: value } (add spaces inside braces)
            s = normalize_all_braces(&s);
            // Normalize import spacing
            s = normalize_import_braces(&s);
            s
        })
        .filter(|l| !l.is_empty())
        .collect::<Vec<_>>()
        .join("\n")
}

fn normalize_all_braces(line: &str) -> String {
    let mut result = String::new();
    let chars: Vec<char> = line.chars().collect();
    let mut i = 0;
    while i < chars.len() {
        if chars[i] == '{' {
            let mut depth = 1;
            let mut j = i + 1;
            while j < chars.len() && depth > 0 {
                if chars[j] == '{' {
                    depth += 1;
                }
                if chars[j] == '}' {
                    depth -= 1;
                }
                j += 1;
            }
            if depth == 0 {
                let inner: String = chars[i + 1..j - 1].iter().collect();
                let inner_trimmed = inner.trim();
                if !inner_trimmed.contains('{') && !inner_trimmed.contains('}') {
                    result.push_str(&format!("{{ {} }}", inner_trimmed));
                    i = j;
                    continue;
                }
            }
            result.push(chars[i]);
            i += 1;
        } else {
            result.push(chars[i]);
            i += 1;
        }
    }
    result
}

fn normalize_import_braces(line: &str) -> String {
    line.to_string() // already handled by normalize_all_braces
}

fn extract_code_block(md: &str) -> Option<String> {
    let code_header_idx = md.find("## Code")?;
    let rest = &md[code_header_idx..];
    let block_start = rest.find("```")?;
    let after_start = &rest[block_start + 3..];
    let newline = after_start.find('\n')?;
    let code_start = &after_start[newline + 1..];
    let block_end = code_start.find("```")?;
    Some(code_start[..block_end].to_string())
}

